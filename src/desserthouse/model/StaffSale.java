package desserthouse.model;

public class StaffSale {
	String id;
	String name;
	int num;
	
	public void setId(String s){
		this.id = s;
	}
	public String getId(){
		return this.id;
	}
	public void setNum(int i){
		this.num = i;
	}
	public int getNum(){
		return this.num;
	}
	public void setName(String s){
		this.name = s;
	}
	public String getName(){
		return this.name;
	}
}
